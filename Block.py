##-------------------------------------------------------------------------------------------------##
class Block:
    def __init__(self):
        self.__x = 0
        self.__y = 0
    ##--------------------------------------------------------------------------------------------##
    ## block의 좌표 반환
    ##--------------------------------------------------------------------------------------------##
    def getPos(self):
        return self.__x, self.__y
    ##--------------------------------------------------------------------------------------------##
    ## block의 좌표 설정
    ##--------------------------------------------------------------------------------------------##
    def setPos(self, x, y):
        self.__x = x
        self.__y = y